    la integridad de sus usuarios. Cuando le pedimos llenar los campos de información personal, o
    formularios, para que usted pueda ser identificado, lo hacemos asegurando que sólo se empleará de
    acuerdo con los términos de este documento. Sin embargo, esta Política de Privacidad puede cambiar o
    ser actualizada cada cierto tiempo, por lo que le recomendamos revisar continuamente esta página para
    asegurarse que está de acuerdo con dichas modificaciones. Es importante que nuestros usuarios
    comprendan que las disposiciones aquí expresadas forman parte integrante de los <span>Términos y
    Condiciones de BienFino</span> y que al crear su Cuenta de Usuario y aceptarlos, también está aceptando el
    contenido de las Políticas de Privacidad.</p>

    <p><span> Cuenta del Usuario</span>, es un conjunto de datos que identifica al usuario con el sistema, información
    protegida con algún protocolo de seguridad (en este con una <span> de Acceso</span>). Una <span>Sesión del
    Cliente</span>, es cuando el usuario accede al sistema (previa comprobación de identidad) y este muestra y
    registra la información del cliente, de acuerdo con las características registradas en su <span> del
    Usuario.</span></p>

